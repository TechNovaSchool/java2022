package Review;

public class PersonalInfoPage {

    String firstName;
    String lastName;
    String email;
    long SSN;
    String cellNumber;
    String DOB;

    public PersonalInfoPage(String firstName, String lastName, String email, long SSN, String cellNumber, String DOB) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.SSN = SSN;
        this.cellNumber = cellNumber;
        this.DOB = DOB;
    }
}
